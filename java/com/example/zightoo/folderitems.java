package com.example.zightoo;

public class folderitems {
    String label;
    int image;
    public folderitems(String label, int image) {
        this.label = label;
        this.image = image;
    }



    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
